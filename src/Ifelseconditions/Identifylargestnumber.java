package Ifelseconditions;

public class Identifylargestnumber {
    public static void main(String[] args) {
        int a = 10;
        int b = 20;

        if (a > b) {
            System.out.println("The Largest number is " + a);
        } else {
            System.out.println("The Smallest number is " + b);
        }
    }
}