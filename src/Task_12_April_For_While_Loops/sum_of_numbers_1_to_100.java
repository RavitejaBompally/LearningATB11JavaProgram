package Task_12_April_For_While_Loops;

public class sum_of_numbers_1_to_100 {
    public static void main(String[] args) {
        int sum = 0;
        for (int i = 1; i <= 100; i++) {
            sum = sum + i;
        }
            System.out.println("the sum of numbers from 1 to 100 " + sum);
        }
    }
