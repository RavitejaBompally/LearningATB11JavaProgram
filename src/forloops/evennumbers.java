package forloops;

public class evennumbers {
    public static void main(String[] args) {
        for (int i = 2; i <= 50; i++) {
            if (i%2==0) {
                System.out.println("Printing the even numbers " + i);
            }
            else {
                System.out.println("Print odd numbers " + i);
            }
            }
        }
    }
